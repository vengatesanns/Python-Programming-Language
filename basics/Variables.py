# Best Practices

# 1) Should be snake case
total_amount = 10000
print(total_amount)

# 2) start with lower or underscore
employee_name = 'Vengat'
_employee_id = 100  # Private Variable

print(employee_name)
print(_employee_id)

# 3) Case-Sensitive
employee_Name = 'Vengat'
employee_name = 'Mano'
print(employee_name)
print(employee_Name)

# 4) Don not override Keyword
# elif = "vengat"
# print(elif)

# 5) CONSTANTS (Maintain ALL CAPS there is no hard rule)
USER_KEY = "user_key"

print(USER_KEY)
